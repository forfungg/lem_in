#include <stdio.h>
#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int len;

	len = ft_strlen(s);
	while (len >= 0)
	{
		if (c == s[len])
			return ((char*)&s[len]);
		len--;
	}
	return (0);
}
